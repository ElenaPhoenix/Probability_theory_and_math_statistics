# –ü—Ä–∏–º–µ—Ä: –ø—Ä–æ–¥–∞–≤–µ—Ü —É—Ç–≤–µ—Ä–∂–¥–∞–µ—Ç, —á—Ç–æ –æ–Ω –∏–∑–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ—Ç –¥–µ—Ç–∞–ª–∏ —Ä–∞–∑–º–µ—Ä–æ–º 10 –º–º. –í–∑—è—Ç–∞ –≤—ã–±–æ—Ä–∫–∞ –∏–∑ 12 –¥–µ—Ç–∞–ª–µ–π. –°–∏–≥–º–∞ –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–∞. 
import numpy as np
from scipy import stats

x = np.array([10.50, 9.94, 10.42, 10.47, 10.4, 9.93, 9.17, 9.26, 10.11, 10.15, 10.5, 10.47])

print(np.mean(x)) # —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ 10,11
# –°–∏–≥–º–∞=10.11 > —Å–∏–≥–º–∞=10, –∑–Ω–∞—á–∏—Ç –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∞ –ø—Ä–∞–≤–µ–µ –Ω—É–ª–µ–≤–æ–π.
# –ù—É–∂–Ω–æ —Ç–∞–±–ª–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∫—Ä–∏—Ç–µ—Ä–∏—è –°—Ç—å—é–¥–µ–Ω—Ç–∞ —Å—Ä–∞–≤–Ω–∏—Ç—å —Å —Ä–∞—Å—á–µ—Ç–Ω—ã–º –∑–Ω–∞—á–µ–Ω–∏–µ–º –∫—Ä–∏—Ç–µ—Ä–∏—è –°—Ç—å—é–¥–µ–Ω—Ç–∞
# –°—á–∏—Ç–∞–µ–º —Å–∏–≥–º—É –Ω–µ—Å–º–µ—â–µ–Ω–Ω—É—é
print(np.std(x, ddof=1)) # –Ω–µ—Å–º–µ—â–µ–Ω–Ω–æ–µ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ 0,468
# ùë°–Ω=(ùëã¬†ÃÖ‚àíŒº)/(ùûº–Ω/‚àöùëõ)
# ùûº–Ω- —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –Ω–µ—Å–º–µ—â–µ–Ω–Ω–æ–µ  –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ,  —Ä–∞—Å—Å—á–∏—Ç–∞–Ω–Ω–æ–µ –ø–æ –≤—ã–±–æ—Ä–∫–µ
t = (np.mean(x) - 10) / (np.std(x, ddof=1) / (len(x))**0.5)
print(t) # 0,814
# –ü–æ —Ç–∞–±–ª–∏—Ü–µ –Ω–∞—Ö–æ–¥–∏–º —á–∏—Å–ª–æ —Å—Ç–µ–ø–µ–Ω–µ–π —Å–≤–æ–±–æ–¥—ã, —Ä–∞–≤–Ω–æ–µ –æ–±—ä–µ–º—É –≤—ã–±–æ—Ä–∫–∏ - 1, —Ç.–µ. 12 - 1 = 11
# –°–º–æ—Ç—Ä–∏–º –∫–∞–∫–∞—è —É –Ω–∞—Å –ø–ª–æ—â–∞–¥—å, –µ—Å–ª–∏ –∞–ª—å—Ñ–∞ = 0,05, —Ç–æ –Ω—É–∂–Ω–∞—è –ø–ª–æ—â–∞–¥—å = 0,95 => t—Ç = 1,8
# t–Ω=0.814 < t—Ç=1,8, –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∞ –ø—Ä–∞–≤–µ–µ –Ω—É–ª–µ–≤–æ–π => —Ä–∞—Å—á–µ—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –ø–æ–ø–∞–¥–∞–µ—Ç –≤ –æ–±–ª–∞—Å—Ç—å –ø—Ä–∏–Ω—è—Ç–∏—è –Ω—É–ª–µ–≤–æ–π –≥–∏–ø–æ—Ç–µ–∑—ã

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ç–µ—Å—Ç–∞ –°—Ç—å—é–¥–µ–Ω—Ç–∞
print(stats.ttest_1samp(x, 10)) # 1 - —ç—Ç–æ –æ–¥–Ω–æ–≤—ã–±–æ—Ä–æ—á–Ω—ã–π —Ç–µ—Å—Ç
# TtestResult(statistic=0.8136488014166606, pvalue=0.43310585815519953, df=11) # –µ—Å–ª–∏ pvalue > –∞–ª—å—Ñ–∞=5% => –≤—ã–±–∏—Ä–∞–µ–º 0-—É—é –≥–∏–ø–æ—Ç–µ–∑—É
# –¥–≤—É—Ö–≤—ã–±–æ—Ä–æ—á–Ω—ã–π —Ç–µ—Å—Ç —Å –Ω–µ–∑–∞–≤–∏—Å–∏–º—ã–º–∏ –≤—ã–±–æ—Ä–∫–∞–º–∏ stats.ttest_ind()
# –¥–≤—É—Ö–≤—ã–±–æ—Ä–æ—á–Ω—ã–π —Ç–µ—Å—Ç —Å –∑–∞–≤–∏—Å–∏–º—ã–º–∏ –≤—ã–±–æ—Ä–∫–∞–º–∏ stats.ttest_rel()

# –í—ã–≤–æ–¥: —Ä–∞–∑–º–µ—Ä –¥–µ—Ç–∞–ª–µ–π 10 –º–º –Ω–∞ —É—Ä–æ–≤–Ω–µ –∑–Ω–∞—á–∏–º–æ—Å—Ç–∏ 5%